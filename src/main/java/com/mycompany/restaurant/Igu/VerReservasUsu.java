    private void cargarTabla() {
        //definimos el modelo que queremos la tabla
        DefaultTableModel modeloTabla = new DefaultTableModel() {
            // decidimos que las filas y colunas no sean editables
            @Override
            public boolean isCellEditable(int row, int column) {

                return false;

            }

        };
        //Establecemos el titulo para cada columna
        Object titulo[] = {"Id_Reserva", "Fecha", "Hora", "Estado", "mesa", "Capacidad"};
        modeloTabla.setColumnIdentifiers(titulo);
    
     //Encontramos todas las reservas de la bd
        List<ReservaUsuarios> lista = cont9.traerReservas();
        
      for (ReservaUsuarios list : lista) {
          
    if (list != null && list.getUsu() != null) { // validamos que la lista no venga vacia ni la relacion con el usuario
        
        if (list.getUsu().getId() == usu.getId()) { 
            
            // Formato de fecha
            SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
            String fecha = formato.format(list.getFecha());

            // Validamos que la mesa no sea null antes de acceder a sus datos
            Object objeto[] = {
                list.getId(),
                fecha,
                list.getHora(),
                list.getEstado(),
                list.getMesa() != null ? list.getMesa().getId() : null,
                list.getMesa() != null ? list.getMesa().getCapacidad() : null
            };

            modeloTabla.addRow(objeto);
        }
    }
}

     //Cambiamos el modelo de la tabla
        Tabla.setModel(modeloTabla);

    }
