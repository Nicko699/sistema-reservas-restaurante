    private void btnEliminarMesasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarMesasActionPerformed
       // Verificamos si la tabla tiene filas
if (Tabla.getRowCount() > 0) {
    
    // Verificamos si se ha seleccionado alguna fila
    if (Tabla.getSelectedRow() != -1) {
        
        // Obtenemos el id de la reserva de la columna 0 de la fila seleccionada
        int id_Reserva = Integer.parseInt(String.valueOf(Tabla.getValueAt(Tabla.getSelectedRow(), 0)));
        
        // Llamamos al controlador para eliminar la reserva correspondiente
        cont9.eliminarReserva(id_Reserva);
        
        // Mostramos un mensaje de éxito
        mostrarMensaje("Reserva eliminada", "Info", "Eliminación Exitosa");
        
        // Recargamos la tabla para reflejar los cambios
        cargarTabla();
        
    } else {
        // mostramos un mensaje de error
        mostrarMensaje("Tabla Vacía", "Error", "Error al eliminar");
    }

} else {
    // mostramos un mensaje de error por no seleccionar una reserva
    mostrarMensaje("No se selecciono una reserva", "Error", "Error al eliminar");
}
    }

    public void mostrarMensaje(String mensaje, String tipo, String titulo) {
       
    // Creamos un objeto JOptionPane con el mensaje que queremos mostrar
    JOptionPane optionPane = new JOptionPane(mensaje);

    // Configuramos el tipo de mensaje según el parámetro 
    // mensajes personalizados
    if (tipo.equals("Info")) {
        optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
    } else if (tipo.equals("Error")) {
        optionPane.setMessageType(JOptionPane.ERROR_MESSAGE);
    }

    JDialog dialog = optionPane.createDialog(titulo);
    dialog.setAlwaysOnTop(true);
    dialog.setVisible(true);

    }//GEN-LAST:event_btnEliminarMesasActionPerformed

